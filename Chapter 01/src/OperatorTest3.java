
public class OperatorTest3 {

	public static void main(String[] args) {
		
		int score1 = 10;
		int score2 = 3;
		
		System.out.println(score1 > score2);
		System.out.println(score1 >= score2);
		System.out.println(score1 < score2);
		System.out.println(score1 <= score2);
		System.out.println(score1 == score2);
		System.out.println(score1 != score2);
	}

}
